function parseIELTS4000(text) {
  const lines = text.replaceAll("\r\n", "\n").split("\n");
  const entries = [];
  let current = null;

  for (const rawLine of lines) {
    const line = rawLine.trim();
    if (!line || line === "IELTS" || line === "4000 Academic Word List") continue;
    if (/^[A-Z]$/.test(line)) continue;

    const match = line.match(/^([a-z][a-z-]*):\s*(.+)$/);
    if (match) {
      if (current) entries.push(current);
      current = { word: match[1].toLowerCase(), definition: cleanDefinition(match[2]) };
      continue;
    }

    if (current) {
      current.definition = cleanDefinition(`${current.definition} ${line}`);
    }
  }

  if (current) entries.push(current);
  return entries;
}

function cleanDefinition(value) {
  return value
    .replace(/\s+/g, " ")
    .replace(/\s+;/g, ";")
    .replace(/;\s*/g, "; ")
    .trim();
}

function makeExample(word) {
  return `Try to use "${word}" in your own IELTS sentence.`;
}

function inferTheme(word, definition) {
  const text = `${word} ${definition}`.toLowerCase();
  const rules = [
    ["education", ["school", "student", "teacher", "education", "academic", "learn", "study", "college", "university"]],
    ["environment", ["climate", "environment", "pollution", "animal", "plant", "earth", "soil", "water", "forest"]],
    ["technology", ["computer", "machine", "software", "digital", "device", "network", "technology", "data"]],
    ["health", ["health", "disease", "medical", "body", "mental", "doctor", "pain", "treatment"]],
    ["economy", ["money", "business", "trade", "industry", "market", "economic", "labor", "labour", "price"]],
    ["government", ["law", "government", "policy", "authority", "court", "legal", "public", "state"]],
    ["society", ["people", "social", "community", "culture", "family", "human", "society"]],
    ["writing", ["show", "indicate", "cause", "result", "effect", "contrast", "increase", "decrease"]],
  ];

  for (const [theme, keywords] of rules) {
    if (keywords.some(keyword => text.includes(keyword))) return theme;
  }

  return "general";
}

async function loadECDICT(path) {
  try {
    const text = await readFile(path, "utf8");
    const lines = text.replaceAll("\r\n", "\n").split("\n");
    const map = new Map();

    for (let i = 1; i < lines.length; i += 1) {
      const line = lines[i];
      if (!line.trim()) continue;
      const fields = parseCsvLine(line);
      const word = fields[0]?.toLowerCase();
      if (!word || map.has(word)) continue;
      map.set(word, {
        phonetic: fields[1] || "",
        definition: fields[2] || "",
        translation: cleanTranslation(fields[3] || ""),
      });
    }

    return map;
  } catch {
    return new Map();
  }
}

function parseCsvLine(line) {
  const fields = [];
  let current = "";
  let quoted = false;

  for (let i = 0; i < line.length; i += 1) {
    const char = line[i];
    const next = line[i + 1];

    if (char === '"' && quoted && next === '"') {
      current += '"';
      i += 1;
      continue;
    }

    if (char === '"') {
      quoted = !quoted;
      continue;
    }

    if (char === "," && !quoted) {
      fields.push(current);
      current = "";
      continue;
    }

    current += char;
  }

  fields.push(current);
  return fields;
}

function cleanTranslation(value) {
  return value
    .replace(/\\n/g, "；")
    .replace(/\n/g, "；")
    .replace(/\s+/g, " ")
    .replace(/；\s*/g, "；")
    .replace(/^；+|；+$/g, "")
    .trim();
}
