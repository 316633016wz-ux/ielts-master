import { readFile, writeFile } from "fs/promises";
import { dirname, join } from "path";
import { fileURLToPath } from "url";

const root = dirname(dirname(fileURLToPath(import.meta.url)));
const sourcePath = join(root, "data", "vocab-sources", "IELTS-4000.txt");
const outputPath = join(root, "frontend", "js", "utils", "ielts-4000-data.js");

const raw = await readFile(sourcePath, "utf8");
const entries = parseIELTS4000(raw)
  .filter(item => /^[a-z][a-z-]*$/.test(item.word))
  .map(item => ({
    id: `ielts4000-${item.word.replaceAll("-", "-")}`,
    word: item.word,
    phonetic: "",
    meaning: item.definition,
    example: makeExample(item.word),
    theme: inferTheme(item.word, item.definition),
    source: "IELTS-4000",
  }));

const unique = Array.from(new Map(entries.map(item => [item.word, item])).values());

const output = `// Auto-generated from data/vocab-sources/IELTS-4000.txt.
// Source repo: NeuralPensieve/Vocabulary-Knowledge-Thompson-Sampling, MIT License.
// Do not edit this file by hand. Run: npm run vocab:import

export const IELTS_4000_VERSION = "2026-07-02-import";

export const IELTS_4000_SOURCE = {
  name: "IELTS-4000 Academic Word List",
  repository: "https://github.com/NeuralPensieve/Vocabulary-Knowledge-Thompson-Sampling",
  license: "MIT",
  localFile: "data/vocab-sources/IELTS-4000.txt",
};

export const IELTS_4000_DATABASE = ${JSON.stringify(unique, null, 2)};
`;

await writeFile(outputPath, output, "utf8");
console.log(`Imported ${unique.length} IELTS words to ${outputPath}`);

function parseIELTS4000(text) {
  const lines = text.replaceAll("\r\n", "\n").split("\n");
  const entries = [];
  let current = null;

  for (const rawLine of lines) {
    const line = rawLine.trim();
    if (!line || line === "IELTS" || line === "4000 Academic Word List") continue;
    if (/^[A-Z]$/.test(line)) continue;

    const match = line.match(/^([a-z][a-z-]*):\s*(.+)$/);
    if (match) {
      if (current) entries.push(current);
      current = { word: match[1].toLowerCase(), definition: cleanDefinition(match[2]) };
      continue;
    }

    if (current) {
      current.definition = cleanDefinition(`${current.definition} ${line}`);
    }
  }

  if (current) entries.push(current);
  return entries;
}

function cleanDefinition(value) {
  return value
    .replace(/\s+/g, " ")
    .replace(/\s+;/g, ";")
    .replace(/;\s*/g, "; ")
    .trim();
}

function makeExample(word) {
  return `Try to use "${word}" in your own IELTS sentence.`;
}

function inferTheme(word, definition) {
  const text = `${word} ${definition}`.toLowerCase();
  const rules = [
    ["education", ["school", "student", "teacher", "education", "academic", "learn", "study", "college", "university"]],
    ["environment", ["climate", "environment", "pollution", "animal", "plant", "earth", "soil", "water", "forest"]],
    ["technology", ["computer", "machine", "software", "digital", "device", "network", "technology", "data"]],
    ["health", ["health", "disease", "medical", "body", "mental", "doctor", "pain", "treatment"]],
    ["economy", ["money", "business", "trade", "industry", "market", "economic", "labor", "labour", "price"]],
    ["government", ["law", "government", "policy", "authority", "court", "legal", "public", "state"]],
    ["society", ["people", "social", "community", "culture", "family", "human", "society"]],
    ["writing", ["show", "indicate", "cause", "result", "effect", "contrast", "increase", "decrease"]],
  ];

  for (const [theme, keywords] of rules) {
    if (keywords.some(keyword => text.includes(keyword))) return theme;
  }

  return "general";
}
